// Open Keyword

// Classes and functions are final by default. Use open to allow inheritance.

open class Base
class Child : Base()

final class A // not required. it is final by default.

// however it is needed for explicitly preventing further overriding in
// subclasses of `Child`
open class Base {
    open fun work() {}
}

class Child : Base() {
    final override fun work() {} // <----- here
}

// Reflections

// Allows inspecting classes at runtime. i.e.

// - Class structure
// - Property names
// - Functions
// - Annotations

// can be accessed. in simpler words, the type information not instance information.

class User

val kClass = User::class // User::class gives you a KClass<User> object
                         // that represents metadata about the class at
                         // runtime

println(kClass.simpleName) // prints `User`
println(kClass.qualifiedName) // prints `com.example.User`
