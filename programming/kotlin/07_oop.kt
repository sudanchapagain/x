// Visibility Modifier

// - public (default)
// - Private: restricted to its own scope.
// - Protected: visible inside the class and its sub-classes. Not visible outside, even in the same package.
// - Internal: Visible within the same module only. Useful for multi-module projects. A module is usually a Gradle module.

internal class Repo

